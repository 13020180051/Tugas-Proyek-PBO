        String uts = txtuts.getText();
        String kuis = txtNK.getText();
        String uas = txtuas.getText();
        String tp = txtTP.getText();
        
        if(JM.isEmpty()){
            JOptionPane.showMessageDialog(this, "Masukan Jumlah Mahasiswa Terlebih Dahulu");
            txtJM.requestFocus();
        }else{
            String[] data = {hadir, uts, kuis, uas,tp};
            
            String jml = calculate(data);
            Object[] data2 = {JM, hadir, uts, kuis, uas,tp, jml, calAbjad(jml)};
            model.addRow(data2);
        }
        
