    private String calculate(String... utm){
        for (int i = 0; i < utm.length; i++){         jTable1.setRowHeight(30);

                if(utm[i].isEmpty()){
                    utm[i] = "0";
                }
        }
        double t1=0,t2=0,t3=0,t4=0,t5=0;
        for (int i = 0; i < utm.length; i++) {
            double cv = Double.valueOf(utm[i]);
            switch (i) {
                case 0:
                    t1 = cv * 0.15;
                    break;
                case 1:
                    t2 = cv * 0.2;
                    break;
                case 2:
                    t3 = cv * 0.2;
                    break;
                case 3:
                    t4 = cv * 0.25;
                    break;
                case 4:
                    t5 = cv * 0.2;
                    break;
                default:
                    break;
            }
        }
        double jumlah = t1+t2+t3+t4+t5;
        double as = Math.round(jumlah);
        String cvJumlah = indexOF(as);
        return cvJumlah;
    }
    
    public String indexOF(double b){
        String hsl;
        if(b %1 == 0){
            hsl = String.valueOf(b).substring(0,String.valueOf(b).lastIndexOf("."));
        }else{
            hsl = String.valueOf(b);
        }
        return hsl;
    }
    
    private String calAbjad(String jumlah){
        String grade = "";
        double nilai = Double.valueOf(jumlah);
        if ( nilai >= 85 ) {
            grade = "A";

        } else if ((nilai<85)&&(nilai>=81)){
            grade = "A-";
        } else if ((nilai<=80)&&(nilai>=76)){
            grade = "B+";
        } else if ((nilai<=75)&&(nilai>=71)) {
            grade = "B";
            
        } else if ((nilai<=70)&&(nilai>=66)){
            grade = "B-";
        } else if ((nilai<=65)&&(nilai>=61)){
            grade = "C+";
        } else if ((nilai<=60)&&(nilai>=51)){
            grade = "C";
            
        }else if ((nilai<=50)&&(nilai>=45)){
            grade = "D";
            
        } else {
            grade = "E";
        }   
        return grade;
    }    
    
   
